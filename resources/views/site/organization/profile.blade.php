                                    <h3>KOPERASI PRODUSEN AMAN MANDIRI (KPAM)</h3>
                                    <div class="paragraph p-t-20">
                                        <p>&nbsp;</p>
                                        <p>Sebagai perwujudan untuk menuju cita-cita kemandirian Ekonomi Masyarakat Adat, KPAM sebagai lembaga profit dan sosial benefit akan terus memacu semangat mewujudkan masyarakat adat yang berdaulat secara politik, mandiri secara ekonomi dan bermartabat secara budaya.</p>
                                        <p>&nbsp;</p>
                                        <p>Dengan semangat tersebut diatas, tepatnya pada tanggal 12 Februari 2013 di Palangkaraya, Kalimantan Tengah, AMAN telah mendirikan Badan Otonom yaitu&nbsp; Koperasi AMAN Mandiri (KAM) yang dihadiri oleh sekitar 200-an orang peserta. Dalam rapat tersebut hadir juga&nbsp; Direktur Hukum dan Kelembagaan Kementerian Koperasi &amp; UKM RI beserta staff ahli Kelembagaan Menteri Koperasi sebagai wakil dari pemerintah untuk menjadi saksi pendirian KAM. Pada rapat tersebut disepakati untuk membentuk dan mendirikan Koperasi, memilih Pengurus dan Pengawas, serta menyusun program kerja. Diharapkan koperasi yang di lahirkan menjadi lokomotif ekonomi masyarakat adat yang mampu melayani anggotanya dan menjawab kebutuhan masyarakat adat, sesuai dengan potensi yang dimiliki dan pengelolaan yang berkelanjutan, sebagai model&nbsp; implementasi jalan baru kemandirian ekonomi indonesia, dari ekonomi berbasis sumberdaya alam hayati menuju ekonomi kreatif berbasis budaya Masyarakat Adat Nusantara.</p>
                                        <p>&nbsp;</p>
                                        <p>Dalam perjalanan sejak KAM&nbsp; dideklarasikan, pengurus KAM berusaha untuk mengurus berbagai Legalitas yaitu Akte Notaris, Pengesahan dari Kementerian Koperasi dan UKM, SIUP dan TDP. Pengurusan legalitas koperasi mengalami kendala selama lebih dari&nbsp; satu tahun setengah terutama di Akte Notaris dan Pengesahan dari Kementerian Koperasi dan UKM. Pasca MK membatalkan UU Koperasi Nomor 25 dan kembali ke UU Nomor 17, KAM kemudian melakukan pembaruan dengan memulai dari awal yaitu proses sosialisasi, memilih anggota ulang dan kemudian&nbsp; &nbsp;memilih jenis Koperasi yaitu Koperasi Produsen. Setelah proses internal dilakukan kemudian KAM berobah menjadi KPAM (Koperasi Produsen AMAN Mandiri) yang pada bulan November tahun 2014 telah memiliki Akte Notaris, kemudian mendapatkan SK Pengesahan Menteri Koperasi dan UKM, Telah Memilik SIUP dan TDP. Secara legalitas atministrasi KPAM telah siap dan menjadi perusahaan kolektif yang dapat beroperasi dan siap berkompetisi.&nbsp;</p>
                                        <p>&nbsp;</p>
                                        <p>Beberapa aktifitas unit usaha nasional dan unit usaha cabang &nbsp;yang di kembangkan oleh KPAM &nbsp;antara lain ; Gerai Nusantara, Travel Nusantara Kita, Nusantara Indigenous Coffee, Pinjaman Modal Usaha Anggota, Penyertaan Modal Usaha Cabang dan Toko Kita. KPAM sebagai lembaga yang berbadan hukum koperasi primer Nasional &nbsp;dapat membentuk cabang-cabang di tingkat Wilayah, Daerah dan Komunitas Masyarakat Adat. KPAM juga dapat menjadi salah satu model kelembagaan yang bangun oleh masyarakat adat dalam bentuk &nbsp;&nbsp;Badan Usaha Milik Masyarakat Adat (BUMMA) atau membangun sinergi&nbsp; bersama mewujudkan salah satu pilar yang dimandatkan kepada organisasi AMAN, yaitu Masyarakat Adat yang Mandiri secara Ekonomi. Kemandirian ekonomi Masyarakat Adat dalam mengelola kekayaan yang berada didalam wilayah adat dikelola melalui Musyarawarah Adat, Gotong Royong, Adil Lestari dan Berbagi Hasil. Fokus yang menjadi tugas KPAM atau dan BUMMA adalah &nbsp;:</p>
                                        <p>&nbsp;</p>
                                        <p>Mengkonsolidasikan dan memproduksi berbagai&nbsp; produk yang terdapat di wilayah adat</p>
                                        <ol>
                                        <li>Membangun kelembagaan usaha yang kuat dan berkelanjutan</li>
                                        <li>Membuka akses pasar seluas -luasnya bagi produk-produk yang dihasilkan oleh kelompok usaha Masyarakat Adat dan kepastian harga yang menguntungkan.</li>
                                        <li>Memberikan tambahan pendapatan untuk meningkatkan sumber pendapatan ekonomi bagi warga komunitas Masyarakat Adat serta secara khusus tentunya bagi anggota KPAM.</li>
                                        </ol>
                                        <p>&nbsp;</p>
                                        <p><strong>Visi KPAM</strong></p>
                                        <p>Mewujudkan Kemandirian Ekonomi Masyarakat Adat terutama meningkatkan pendapatan &nbsp;yang layak bagi anggota dan &nbsp;masyarakat adat.</p>
                                        <p>&nbsp;</p>
                                        <p><strong>Misi KPAM</strong></p>
                                        <ol>
                                        <li>Terbentuknya usaha-usaha produksi&nbsp; di wilayah adat yang berkelanjutan</li>
                                        <li>Menggalang dan mendukung permodalan (investasi) bagi usaha-usaha KPAM atau usaha kerjasama dengan pihak lain yang sesuai dengan visi dan misi</li>
                                        <li>Membangun kerjasama dengan pemerintah dan lembaga usaha yang sejalan dengan visi dan misi</li>
                                        <li>Memasarkan hasil-hasil usaha produksi dari masyarakat adat</li>
                                        </ol>
                                        <p>&nbsp;</p>
